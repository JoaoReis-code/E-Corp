    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        String numeroCartao = JOptionPane.showInputDialog(
                null,
                "Digite numero do cartao que voce deseja utilizar",
                "Cartao",
                JOptionPane.QUESTION_MESSAGE);

        String valor = JOptionPane.showInputDialog(
                null,
                "Digite quanto voce deseja gastar",
                "Cartao",
                JOptionPane.QUESTION_MESSAGE);

        String senha = JOptionPane.showInputDialog(
                null,
                "Digite a senha do cartao",
                "Cartao",
                JOptionPane.QUESTION_MESSAGE);
        try {
            if (!(valor.isEmpty() || numeroCartao.isEmpty() || senha.isEmpty())) {
                if (numeroCartao.matches("-?[0-9]*(\\.[0-9]+)?")) {
                    Cartao cartao = Crud.buscarCartao(numeroCartao);
                    if (cartao != null) {
                        if (cartao.getTipoCartao().equals(TipoCartao.DEBITO)) {
                            try {
                                cartao.debito(contaLogada, Double.parseDouble(valor), senha);
                                valorTextField.setText("R$ " + String.valueOf(contaLogada.getSaldo()));
                                JOptionPane.showMessageDialog(null, "Pagamento realizado com sucesso", "Cartao", JOptionPane.PLAIN_MESSAGE);
                            } catch (Exception ex) {
                                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.PLAIN_MESSAGE);
                            }
                        } else if (cartao.getTipoCartao().equals(TipoCartao.CREDITO)) {
                            String parcelas = JOptionPane.showInputDialog(
                                    null,
                                    "Digite o numero de parcelas",
                                    "Cartao",
                                    JOptionPane.QUESTION_MESSAGE);
                            try {
                                cartao.credito(Double.parseDouble(valor), senha, Integer.parseInt(parcelas));
                                valorTextField.setText("R$ " + String.valueOf(contaLogada.getSaldo()));
                                JOptionPane.showMessageDialog(null, "Pagamento realizado com sucesso", "Cartao", JOptionPane.PLAIN_MESSAGE);
                            } catch (Exception ex) {
                                JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.PLAIN_MESSAGE);
                            }
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Nao foi possivel encontrar esse cartao", "Erro", JOptionPane.PLAIN_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Digite apenas numeros", "Erro", JOptionPane.PLAIN_MESSAGE);
                }
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jButton22ActionPerformed

